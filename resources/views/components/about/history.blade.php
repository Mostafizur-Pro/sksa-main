            <p class="py-6">松濤館, Shōtōkan is a style of karate, developed from various martial arts by Master Gichin Funakoshi (1868–1957) and his son Gigo (Yoshitaka) Funakoshi (1906–1945). Master Gichin Funakoshi was born in Okinawa and is widely credited with popularizing "KARATE-DO" through a series of public demonstrations, and by promoting the development of University Karate Clubs, including those at Keio, Waseda, Hitotsubashi (Shodai), Takushoku, Chuo, Gakushuin, and Hosei.</p>
            <p class="py-6 font-bold">Shōtōkan (松濤館流)</p>

            <ul class="list-disc ml-6">
                <li>Date Founded: 1928</li>
                <li>Country of Origin: Okinawa, Japan.</li>
                <li>Founder: Gichin Funakoshi (1868–1957) & Yoshitaka Funakoshi (1906–1945)</li>
                <li>Arts Taught: Karate</li>
                <li>Ancestor Schools: Shōrei-ryū, Shōrin-ryū</li>
             
            </ul>
            <p class="pt-6 pb-2 font-bold">Descendant Schools:</p>
            <p class="">Wadō-ryū, Shōtōkai, Chitō-ryū, Shindō jinen-ryū, Yoseikan Karate, Kyokushin, Tang soo do, Taekwondo, Soo Bahk Do.</p>
            <p class="py-6 justify">Master Funakoshi had many students at the University Clubs and outside Dojos, who continued to teach karate after his death in 1957. However, internal disagreements (in particular the notion that competition is contrary to the essence of karate) led to the creation of different organisations—including an initial split between the Japan Karate Association (headed by Masatoshi Nakayama) and the Shotokai (headed by Motonobu Hironishi and Shigeru Egami), followed by many others—so that today there is no single "Shotokan school", although they all bear Funakoshi's influence. As the most widely practiced style, Shotokan is considered a traditional and influential form of Karate-Do.</p>

